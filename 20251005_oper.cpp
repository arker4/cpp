#include <iostream>
using namespace std;

int main() {
    int number1 = 18;
    int number2 = 4;
    
    cout << "num1=" << number1 << endl;
    cout << "num2=" << number2 << endl;
    cout << "num1+num2=" << number1 << "+" << number2 << "=" << number1 + number2 << endl;
    cout << "num1-num2=" << number1 << "-" << number2 << "=" << number1 - number2 << endl;
    cout << "num1*num2=" << number1 << "*" << number2 << "=" << number1 * number2 << endl;
    cout << "num1/num2=" << number1 << "/" << number2 << "=" << number1 / number2 << endl;
    cout << "num1%num2=" << number1 << "%" << number2 << "=" << number1 % number2 << endl;
       
    return 0;
}
